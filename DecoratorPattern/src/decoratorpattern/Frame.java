    public FligthTicket optionalStuffs(JCheckBox box, FligthTicket ticket) {

        switch (box.getActionCommand()) {
            case "meal":
                ticket = new Meal(ticket);
                break;
            case "window":
                ticket = new WindowEdgeTicket(ticket);
                break;
            case "internet":
                ticket = new InternetConnectionTicket(ticket);
                break;
            case "extra":
                ticket = new ExtraLugWeigth(ticket);
                break;
            default:
                ticket = null;
                break;
        }

        return ticket;

    }

